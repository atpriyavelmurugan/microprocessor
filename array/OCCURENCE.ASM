                OUTPUT 2500AD
.ORG 2000H
DATA SEGMENT
STR DB "HELLO"
CHAR DB "P"
RES DB 00H
COUNT EQU 05H
DATA ENDS
CODE SEGMENT
        ASSUME CS:CODE,DS:DATA,ES:DATA
        MOV AX,DATA
        MOV DS,AX
        MOV ES,AX
        LEA DI,STR
        MOV AL,CHAR
        MOV CL,COUNT
        CLD
L2:     SCASB
        JNZ L1
        MOV RES,01H
        JMP L3
L1:     INC DI
        DEC CL
        JNZ L2
        MOV RES,00H
L3:     HLT
CODE ENDS
END
